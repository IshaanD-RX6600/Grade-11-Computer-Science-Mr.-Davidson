my_list = [10, 20, 30, 40, 50]

print("Using positive indices:")
print("my_list[1]:", my_list[1])  

print("\nUsing negative indices:")
print("my_list[-1]:", my_list[-1])  


print("\nSlicing with negative indices:")
print("my_list[-3:]:", my_list[-3:]) 
print("my_list[:-2]:", my_list[:-2]) 

print("\nExplanation:")
print("Negative indices count from the end of the list, where -1 is the last element, -2 is the second last, and so on.")